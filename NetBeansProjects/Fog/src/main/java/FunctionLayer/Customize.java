package FunctionLayer;

/**
 *
 * @author claudia
 */
public class Customize
{
    private int legth;
    private int height;
    private int width;

    public Customize(int legth, int height, int width)
    {
        this.legth = legth;
        this.height = height;
        this.width = width;
    }

    public int getLegth()
    {
        return legth;
    }

    public void setLegth(int legth)
    {
        this.legth = legth;
    }

    public int getHeight()
    {
        return height;
    }

    public void setHeight(int height)
    {
        this.height = height;
    }

    public int getWidth()
    {
        return width;
    }

    public void setWidth(int width)
    {
        this.width = width;
    }
    
    
}
